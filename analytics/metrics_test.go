func TestGenerateMetrics_SkipsLayoutFields(t *testing.T) {
	dt := buildTestDocType("Customer", false,
		field("name", "Data", "Name", true),
		field("spacer", "Section Break", "", false),
		field("col", "Column Break", "", false),
		field("heading", "Heading", "", false),
		field("items", "Table", "Items", false),
	)

	metrics := GenerateMetrics(dt)

	// Should NOT have metrics for layout/table fields.
	for _, m := range metrics {
		if m.Field == "spacer" || m.Field == "col" || m.Field == "heading" || m.Field == "items" {
			t.Errorf("should not generate metric for layout/table field %q", m.Field)
		}
	}
}

func TestGenerateMetrics_MultipleNumericFields(t *testing.T) {
	dt := buildTestDocType("Invoice", false,
		field("subtotal", "Float", "Subtotal", true),
		field("tax", "Currency", "Tax", true),
		field("discount", "Percent", "Discount", true),
	)

	metrics := GenerateMetrics(dt)

	sumCount := 0
	for _, m := range metrics {
		if m.Type == MetricSum {
			sumCount++
		}
	}
	if sumCount != 3 {
		t.Errorf("expected 3 sum metrics for 3 numeric fields, got %d", sumCount)
	}
}

func TestMetricName(t *testing.T) {
	tests := []struct{ input, expected string }{
		{"Work Order", "work_order"},
		{"Customer", "customer"},
		{"estimated_hours", "estimated_hours"},
		{"Total Amount", "total_amount"},
	}
	for _, tc := range tests {
		got := metricName(tc.input)
		if got != tc.expected {
			t.Errorf("metricName(%q) = %q, want %q", tc.input, got, tc.expected)
		}
	}
}

func TestGenerateWorkflowMetrics(t *testing.T) {
	dt := buildTestDocType("Work Order", true)
	wf := &doctype.Workflow{
		Name:         "Work Order Lifecycle",
		DocumentType: "Work Order",
		Transitions: []doctype.WorkflowTransition{
			{Action: "Submit", From: "Draft", To: "Submitted"},
			{Action: "Approve", From: "Submitted", To: "Approved"},
			{Action: "Complete", From: "Approved", To: "Completed"},
		},
	}

	metrics := GenerateWorkflowMetrics(dt, wf)

	if len(metrics) != 3 {
		t.Fatalf("expected 3 transition metrics, got %d", len(metrics))
	}

	// Check naming pattern: <doctype>_avg_<from>_to_<to>_time
	expected := map[string]bool{
		"work_order_avg_draft_to_submitted_time":    false,
		"work_order_avg_submitted_to_approved_time": false,
		"work_order_avg_approved_to_completed_time": false,
	}
	for _, m := range metrics {
		if _, ok := expected[m.Name]; ok {
			expected[m.Name] = true
		}
		if !m.AutoGenerated {
			t.Errorf("workflow metric %s should be auto-generated", m.Name)
		}
	}
	for name, found := range expected {
		if !found {
			t.Errorf("expected workflow metric %s", name)
		}
	}
}

func TestGenerateWorkflowMetrics_NilWorkflow(t *testing.T) {
	dt := buildTestDocType("Customer", false)
	metrics := GenerateWorkflowMetrics(dt, nil)
	if len(metrics) != 0 {
		t.Errorf("nil workflow should return 0 metrics, got %d", len(metrics))
	}
}
