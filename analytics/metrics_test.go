// buildTestDocType creates a minimal DocType for testing metric generation.
func buildTestDocType(name string, isSubmittable bool, fields ...doctype.Field) *doctype.DocType {
	return &doctype.DocType{
		Name:          name,
		IsSubmittable: isSubmittable,
		Fields:        fields,
	}
}

func field(name, ftype, label string, inList bool) doctype.Field {
	return doctype.Field{
		Fieldname:   name,
		Fieldtype:   ftype,
		Label:       label,
		InListView:  inList,
	}
}

func TestGenerateMetrics_EveryDocType_Count(t *testing.T) {
	dt := buildTestDocType("Customer", false,
		field("name", "Data", "Name", true),
	)

	metrics := GenerateMetrics(dt)

	// Every DocType gets a count metric.
	found := false
	for _, m := range metrics {
		if m.Name == "customer_count" && m.Type == MetricCount {
			found = true
			if !m.AutoGenerated {
				t.Error("count metric should be auto-generated")
			}
		}
	}
	if !found {
		t.Error("expected customer_count metric")
	}
}

